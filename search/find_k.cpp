class Solution {
  public:
    int findKRotation(vector<int> &arr) {
        // Code Here
        auto smallest = min_element(arr.begin(), arr.end());
        
        for(int i = 0; i < arr.size(); i++){
            if(*smallest == arr[i]){
                return i;
            }
        }
    }
};


